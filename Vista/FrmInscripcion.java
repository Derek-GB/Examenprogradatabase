    public void setEnts(List<Taller> ents) {
        this.ents = ents;
        if (ents == null || tableModel == null) {
            return;
        }
        tableModel.setNumRows(0);

        ents.forEach(ta -> tableModel.addRow(
                new Object[]{
                    ta.getNombre(),
                    ta.getFecha(),
                    ta.getCapacidad() 
                }
        ));
    }
